CREATE OR REPLACE DYNAMIC TABLE ENERGY_MARKET.SILVER.WEATHER_CLEAN
TARGET_LAG = '1 hour'
WAREHOUSE = ENERGY_WH
AS
SELECT
    RECORD_ID,
    LOCATION_NAME,
    LATITUDE,
    LONGITUDE,
    TIMESTAMP_UTC,
    TEMPERATURE_2M,
    WIND_SPEED_10M,
    WIND_DIRECTION_10M,
    SHORTWAVE_RADIATION,
    CLOUD_COVER,
    PRECIPITATION,
    INGESTED_AT
FROM ENERGY_MARKET.BRONZE.WEATHER_RAW
WHERE TIMESTAMP_UTC IS NOT NULL
  AND TEMPERATURE_2M IS NOT NULL
  AND WIND_SPEED_10M >= 0;
