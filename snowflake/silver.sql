
CREATE OR REPLACE DYNAMIC TABLE ENERGY_MARKET.SILVER.GRID_GENERATION_CLEAN
TARGET_LAG = '1 hour'
WAREHOUSE = ENERGY_WH
AS
SELECT
    RECORD_ID,
    FILTER_ID,
    FILTER_NAME,
    REGION,
    RESOLUTION,
    TIMESTAMP_UTC,
    VALUE_MWH,
    INGESTED_AT,
    CASE
        WHEN FILTER_NAME IN ('Wind Onshore','Wind Offshore','Photovoltaik','Biomasse','Wasserkraft','Sonstige Erneuerbare')
        THEN 'Renewable'
        ELSE 'Conventional'
    END AS ENERGY_CLASS
FROM ENERGY_MARKET.BRONZE.GRID_GENERATION_RAW
WHERE VALUE_MWH IS NOT NULL
  AND VALUE_MWH >= 0
  AND TIMESTAMP_UTC IS NOT NULL;

